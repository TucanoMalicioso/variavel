const diasDasemana=3

if (diasDasemana<=7 && diasDasemana>0) {
    if (diasDasemana==1) {
        console.log("segunda")
    }else if (diasDasemana==2) {
        console.log("terça")
    }else if (diasDasemana==3) {
        console.log("quarta")
    }else if (diasDasemana==4) {
        console.log("quinta")
    }else if (diasDasemana==5) {
        console.log("sexta")
    }else if (diasDasemana==6) {
        console.log("sabado")
    }else if (diasDasemana==7) {
        console.log("domingo")
    }
}else{
    console.log("DIA DA SEMANA NÃO É VALIDO")
}
